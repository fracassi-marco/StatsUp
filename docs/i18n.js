  var SUPPORTED = ['en', 'it', 'fr', 'es'];

  function detectLang() {
    var saved = localStorage.getItem('statsup-lang');
    if (saved && SUPPORTED.indexOf(saved) !== -1) return saved;
    var browser = (navigator.language || 'en').split('-')[0];
    if (SUPPORTED.indexOf(browser) !== -1) return browser;
    return 'en';
  }

  function applyLang(lang) {
    var t = T[lang] || T.en;
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      if (t[key] !== undefined) el.textContent = t[key];
    });
    document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-html');
      if (t[key] !== undefined) el.innerHTML = t[key];
    });
    document.documentElement.lang = lang;
    document.querySelectorAll('.lang-btn').forEach(function (btn) {
      btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });
    localStorage.setItem('statsup-lang', lang);
  }

  function buildSwitcher() {
    return '<div class="lang-switcher">' +
      SUPPORTED.map(function (l) {
        return '<button class="lang-btn" data-lang="' + l + '">' + l.toUpperCase() + '</button>';
      }).join('') +
      '</div>';
  }

  document.addEventListener('DOMContentLoaded', function () {
    document.querySelectorAll('.lang-switcher-slot').forEach(function (slot) {
      slot.innerHTML = buildSwitcher();
    });
    document.querySelectorAll('.lang-btn').forEach(function (btn) {
      btn.addEventListener('click', function () { applyLang(btn.getAttribute('data-lang')); });
    });
    applyLang(detectLang());
  });

  window.i18nT = function (key) {
    var lang = localStorage.getItem('statsup-lang') || detectLang();
    var t = T[lang] || T.en;
    return t[key] !== undefined ? t[key] : key;
  };
  window.i18nLatest = function () { return localStorage.getItem('statsup-lang') || detectLang(); };
